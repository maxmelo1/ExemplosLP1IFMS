    private class TratadorFonte implements ActionListener{

        @Override
        public void actionPerformed(ActionEvent ae) {
            
            name = ae.getActionCommand();
            

            Font f = new Font(name, estilo, 70);
            lblAmostra.setFont(f );
        }
        
    }
    
    private class TratadorEstilo implements ActionListener{

        @Override
        public void actionPerformed(ActionEvent ae) {
            
            int estilo = Font.PLAIN;
            

            if(chkItalico.isSelected())
                estilo += Font.ITALIC;
            
            if(chkNegrito.isSelected())
                estilo += Font.BOLD;
            
            
            Font f = new Font(name, estilo, 70);
            lblAmostra.setFont(f );
        }
        
    }
    

